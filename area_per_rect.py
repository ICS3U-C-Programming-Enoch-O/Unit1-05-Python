# This program calculates the area & perimeter of a 7cm X 2cm rectangle.


def main():
    print("For a rectangle with length 7cm and width 2cm:")

    print("The area is: {}cm^2".format(7 * 2))
    print()

    print("The perimeter is: {}cm".format(2 * (7 + 2)))


if __name__ == "__main__":
    main()
